function closeBrowser() {
    const command =
      process.platform === "win32"
        ? 'taskkill /im "chrome.exe" /f'
        : process.platform === "darwin"
        ? 'pkill "Google Chrome"'
        : "killall chrome";
  
    exec(command, (error, stdout, stderr) => {
      if (error) {
        console.error(`Error al intentar cerrar el navegador: ${error.message}`);
        return;
      }
      if (stderr) {
        console.error(`Error: ${stderr}`);
        return;
      }
      console.log("El navegador se cerró correctamente.");
    });
  }

function openBrowser(url) {
  const command =
    process.platform === "win32"
      ? "start chrome"
      : process.platform === "darwin"
      ? 'open -a "Google Chrome"'
      : "xdg-open";

  exec(`${command} ${url}`, (error, stdout, stderr) => {
    if (error) {
      console.error(`Error al intentar abrir el navegador: ${error.message}`);
      return;
    }
    if (stderr) {
      console.error(`Error: ${stderr}`);
      return;
    }
    console.log("El navegador se abrió correctamente.");
  });
}


